package mobilepackage;

public class Nokia {
	//In this Nokia class different methods has be created.
	// method with a public accessModifier(accessSpecifier)
public void SMS() {
	System.out.println("Nokia SMS");
}
//method with a private accessModifier
private void copy() {
	System.out.println("Nokia copy");
}
//method with a protected accessModifier
 protected void text() {
	System.out.println("Nokia text");
}
//with out any modifier is call as default
void Click() {
	System.out.println("Nokia Click");
}

}
