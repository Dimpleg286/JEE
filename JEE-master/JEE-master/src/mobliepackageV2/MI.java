package mobliepackageV2;

public class MI {
	//In this samsung class different methods has be created.
		// method with a public accessModifier(accessSpecifier):it can be access to through out project
	public void casting() {
		System.out.println("samsung casting");
	}
	//method with a private accessModifier: it can be access with in the class
	private void browser() {
		System.out.println("samsung browser");
	}
	//method with a protected accessModifier: it can be access samePackage and we can also access to using applying the inheritance
	 protected void GPS() {
		System.out.println("samsung GPS");
	}
	//with out any modifier is call as default:it can be access able with in the package
	void Smartshare() {
		System.out.println("samsung Smartshare");
	}
}
